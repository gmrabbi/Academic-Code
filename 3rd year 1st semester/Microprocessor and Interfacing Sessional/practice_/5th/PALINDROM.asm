ORG 100H

.DATA
N DB 11011001B 
PAL_MSG DB 0DH, "PALINDROM$";
NPAL_MSG DB 0DH, "NOT PALINDROM$";

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    
    MOV AL, N
    MOV CX, 8
    MOV BX, 0
    
    CALCULATION:
        SHR AL, 1
        RCL BH, 1
        LOOP CALCULATION
   CMP BH, N
   JE PALINDROM
   JMP NOT_PALINDROM
   
   PALINDROM:
    LEA DX, PAL_MSG
    MOV AH, 09H
    INT 21H
    JMP EXIT
  NOT_PALINDROM:
    LEA DX, NPAL_MSG
    MOV AH, 09H
    INT 21H
 
 EXIT:    
    MOV AH, 4CH
    INT 21H
    
    MAIN ENDP
END MAIN
RET
