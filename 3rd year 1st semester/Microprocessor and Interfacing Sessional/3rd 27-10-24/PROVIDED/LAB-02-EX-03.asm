ORG 0100h

.CODE

MAIN PROC

; input a character 
MOV AH, 1
INT 21h
MOV BL,AL
; go to a new line with carriage return 
MOV AH, 2 
MOV DL, 0DH 
INT 21h
MOV DL, 0AH 
INT 21h           

CMP BL,'A'
JNGE EXIT
CMP BL, 'Z'
JLE CHAR_UPPER

CMP BL,'a'
JNGE EXIT

CMP BL,'z'
JLE CHAR_LOWER


CHAR_UPPER:
    ADD BL,32
    MOV DL,BL
    MOV CX,5
    JMP OUTPUT_FORWARD
CHAR_LOWER:
    SUB BL,32
    MOV DL,BL
    MOV CX,5
    JMP OUTPUT_FORWARD

OUTPUT_FORWARD:
    INC DL
    MOV AH,2
    INT 21H
    
    LOOP OUTPUT_FORWARD
; go to a new line with carriage return 
MOV AH, 2 
MOV DL, 0DH 
INT 21h
MOV DL, 0AH 
INT 21h
MOV CX,5
MOV DL,BL

OUTPUT_BACKWARD:
    DEC DL
    MOV AH,2
    INT 21H
    
    LOOP OUTPUT_BACKWARD
EXIT:   
    ; return to DOS 
    MOV AH, 4CH 
    INT 21H
MAIN ENDP 
END MAIN
RET
